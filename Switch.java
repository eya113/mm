package lesswitch;

public class Switch {
    public static void main(String[] args) {
        int dayOf = 5;

        switch (dayOf) {
            case 0:
                System.out.println("sun");
               

            case 6:
                System.out.println("suf");
              

            default:
                System.out.println("sul");
                break;
        }
    }
}

